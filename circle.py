import math
def area(r):
    '''
    Возвращает площадь круга
    
        Параметры:
            r (int): радиус
            
        Возвращаемое значение:
            math.pi * r * r: площадь круга
    '''
    return math.pi * r * r
    
    
def perimeter(r):
    '''
    Возвращает периметр круга
    
        Параметры:
            r(int): радиус
            
        Возвращаемое значение:
            2 * math.pi * r: периметр круга
    '''
    return 2 * math.pi * r
